public class Arithmetics {

    public double add(double a, double b) {
        return a + b;
    }

    public double deduct(double a, double b) {
        return a - b;
    }

    public double multiply(double a, double b) {
        return a * b;
    }

    public double div(double a, double b) {
        return a / b;
    }

}
